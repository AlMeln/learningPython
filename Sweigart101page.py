print('Hello ', end='')  # Использовать именнованный аргумент и вместо автоматически добавляемого
# символа новой строки в конец каждой строки, использовать другую строку, задав ее с помощью
# именнованного аргумента end.

print('cats', 'dogs', 'mise')  # Передать функции print() несколько строковых значений.

print('cats', 'dogs', 'mise', sep=', ')  # Вместо пустой строки по умолчанию можно использовать
# другой разделитель, передав функции именнованый аргумент sep.
