spam = ['cat', 'bat', 'rat', 'elephant']  # В качестве индексов разрешается использовать отрицательные значения.
print(spam[-1])  # Отрицательному значению -1 соответствует последний элемент списка.
print(spam[-2])  # Отрицательному значению -2 соответствует предпоследний элемент списка.
print('The ' + spam[-1] + ' is afraid of the ' + spam[-3] + '.')
