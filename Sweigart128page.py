eggs = 'hello'
print(eggs.append('hello'))  # Методы принадлежат к одному определеному типу данных. Метод append() является методом
# списков и может вызываться только для списков. Вызывать метод для других типов данных, например, строки, нельзя.

bacon = 42
bacon.insert(1, 'world')  # Метод insert() также является методом списков и не может вызываться для других типов
# данных, например целых чисел.
